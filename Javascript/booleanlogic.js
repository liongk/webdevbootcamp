var x = 5

x > 10 //false
x < 10 //true
x <= 200 //true
x >= 40 //false

x == "5" //equal to, TRUE
x === "5" //equal and same datatype, FALSE

x != "5"//unequal to, TRUE
x !== "5" //unequal to OR unequal datatype, FALSE

